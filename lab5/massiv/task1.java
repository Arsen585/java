package massiv;
public class task1 {
    public static void main(String[] args) {
        int[] arr = {10, 45, 3, 80, 22};

        int max = arr[0];

        for (int i = 1; i < arr.length; i++) {
            if (arr[i] > max) {
                max = arr[i];
            }
        }

        System.out.println("Ең үлкен элемент: " + max);
    }
}